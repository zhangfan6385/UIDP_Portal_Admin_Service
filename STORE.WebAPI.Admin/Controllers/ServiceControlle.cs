using Microsoft.AspNetCore.Hosting;

namespace STORE.WebAPI.Controllers
{
    [Produces("application/json")]
    [Route("service")]
    public class ServiceControlle : WebApiBaseController
    {
        ServiceModule mm = new ServiceModule();
        /// <summary>
        /// 查询配置信息
        /// </summary>
        /// <returns></returns>
        [HttpGet("fetchServiceList")]
        public IActionResult fetchServiceList(string limit, string page, string SERVICE_CODE, string SERVICE_NAME, string BEGIN_SERVICE_PUBLISHDATE, string END_SERVICE_PUBLISHDATE)
        {
            //UserModule user = new UserModule();
            //string Admin = user.getAdminCode();
            //bool isAdmin = UserId.Equals(Admin);
            //Dictionary<string, object> res = mm.GetPagedTable(isAdmin);
            //return Json(res);
            Dictionary<string, object> d = new Dictionary<string, object>();
            d["limit"] = limit;
            d["page"] = page;
            d["SERVICE_CODE"] = SERVICE_CODE;
            d["SERVICE_NAME"] = SERVICE_NAME;
            d["BEGIN_SERVICE_PUBLISHDATE"] = BEGIN_SERVICE_PUBLISHDATE;
            d["END_SERVICE_PUBLISHDATE"] = END_SERVICE_PUBLISHDATE;
            Dictionary<string, object> res = mm.fetchServiceList(d);
            return Json(res);
        }
        /// <summary>
        /// 新增
        /// </summary>
        /// <param name="value"></param>
        /// <returns></returns>
        [HttpPost("createServiceArticle")]
        public IActionResult createServiceArticle([FromBody]JObject value)
        {
            Dictionary<string, object> d = value.ToObject<Dictionary<string, object>>();
            Dictionary<string, object> r = new Dictionary<string, object>();
            try
            {
                string b = mm.createServiceArticle(d);
                if (b == "")
                {
                    r["message"] = "成功";
                    r["code"] = 2000;
                }
                else
                {
                    r["code"] = -1;
                    r["message"] = b;
                }
            }
            catch (Exception e)
            {
                r["code"] = -1;
                r["message"] = e.Message;
            }
            return Json(r);
        }
